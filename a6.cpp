#include <iostream>  // Подключаем библиотеку для стандартного ввода-вывода (cin, cout)
#include <vector>    // Подключаем вектор для динамического хранения элементов массива
#include <algorithm> // Подключаем библиотеку алгоритмов (в частности, для функции max)

using namespace std; // Используем стандартное пространство имен

// Глобальные переменные для удобства доступа из рекурсивной функции
int n, k;          // n — общее количество элементов в массиве, k — сколько элементов нужно выбрать
int ans = 0;       // Переменная для хранения итогового максимального значения XOR-суммы
vector<int> a;     // Динамический массив (вектор) для хранения исходных чисел

// Рекурсивная функция перебора (бэкктрекинг)
// idx — текущий индекс элемента в массиве a, который мы рассматриваем
// count — количество уже выбранных элементов на данный момент
// current_xor — текущее значение XOR-суммы выбранных элементов
void backtrack(int idx, int count, int current_xor) {
    
    // БАЗОВЫЙ СЛУЧАЙ 1: Если мы уже выбрали ровно k элементов
    if (count == k) {
        // Обновляем глобальный ответ: выбираем максимальное между старым ответом и текущим XOR
        ans = max(ans, current_xor);
        return; // Возвращаемся из рекурсии, дальше идти по этой ветке нет смысла
    }
    
    // БАЗОВЫЙ СЛУЧАЙ 2 (Оптимизация / Отсечение ветвей):
    // idx == n означает, что мы дошли до конца массива, а k элементов так и не набрали.
    // count + (n - idx) < k означает, что даже если мы ВОЗЬМЕМ ВСЕ оставшиеся элементы массива,
    // нам всё равно не хватит до нужного количества k. В обоих случаях продолжать перебор бесполезно.
    if (idx == n || count + (n - idx) < k) {
        return; // Прерываем текущую ветку перебора и возвращаемся назад
    }

    // ВЕТВЬ 1: Принимаем решение ВЗЯТЬ текущий элемент a[idx] в наше подмножество.
    // Переходим к следующему элементу (idx + 1), увеличиваем счетчик взятых элементов (count + 1)
    // и добавляем текущий элемент в XOR-сумму с помощью оператора '^=' (current_xor ^ a[idx]).
    backtrack(idx + 1, count + 1, current_xor ^ a[idx]);

    // ВЕТВЬ 2: Принимаем решение НЕ БРАТЬ (пропустить) текущий элемент a[idx].
    // Переходим к следующему элементу (idx + 1), счетчик взятых элементов остается прежним (count),
    // текущая XOR-сумма также не меняется (current_xor).
    backtrack(idx + 1, count, current_xor);
}

int main() {
    // Включаем оптимизацию стандартных потоков ввода-вывода для ускорения работы программы
    ios_base::sync_with_stdio(0); // Отключаем синхронизацию между потоками ввода-вывода C и C++
    cin.tie(0);                   // Отвязываем cin от cout

    // Считываем значения n и k. Если ввод пустой или некорректный — завершаем программу
    if (!(cin >> n >> k)) return 0;

    a.resize(n); // Выделяем память под n элементов в векторе 'a'
    // Заполняем массив числами из стандартного ввода
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }

    // Запускаем бэкктрекинг с начальными параметрами:
    // начинаем с 0-го индекса, выбрано 0 элементов, стартовая XOR-сумма равна 0
    backtrack(0, 0, 0);

    // После завершения всего перебора в переменной ans окажется максимальный XOR. Выводим его.
    cout << ans << "\n";

    return 0; // Успешное завершение работы программы
}

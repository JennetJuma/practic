#include <bits/stdc++.h> // Подключаем библиотеку со всеми стандартными структурами данных и функциями
using namespace std;    // Используем стандартное пространство имен, чтобы не писать std:: перед cin/cout

int main() {
    // Включаем оптимизацию ввода-вывода для ускорения работы программы
    ios::sync_with_stdio(false); // Отключаем синхронизацию между потоками ввода-вывода C и C++
    cin.tie(nullptr);            // Отвязываем cin от cout, чтобы не очищать буфер вывода перед каждым вводом

    int t;           // Переменная для хранения количества наборов входных данных (тестов)
    cin >> t;        // Считываем количество тестов из стандартного потока ввода

    // Запускаем цикл, который выполнится ровно t раз (пока t уменьшается до нуля)
    while (t--) {
        int sum = 0;         // Переменная для подсчета общей суммы всех 7 чисел в текущем тесте
        int mx = INT_MIN;    // Переменная для поиска максимума. Инициализируем минимально возможным int (-2147483648)
        
        // Запускаем цикл для последовательного чтения ровно 7 целых чисел
        for (int i = 0; i < 7; ++i) {
            int a;           // Временная переменная для хранения текущего считанного числа
            cin >> a;        // Считываем очередное число из строки
            
            sum += a;        // Добавляем текущее число к общей сумме всех элементов (sum = sum + a)
            mx = max(mx, a); // Обновляем максимум: если текущее число 'a' больше 'mx', то 'mx' становится равен 'a'
        }
        
        // Формула математического фокуса:
        // Если бы мы поменяли знак у ВСЕХ 7 чисел, сумма стала бы равной (-sum).
        // Но нам нужно оставить знак нетронутым у ОДНОГО (максимального) числа 'mx'.
        // Значит, вместо (-mx) в нашей тотально инвертированной сумме должно быть (+mx).
        // Разница между (+mx) и (-mx) составляет ровно 2 * mx.
        // Поэтому мы берем общую инвертированную сумму (-sum) и прибавляем к ней 2 * mx.
        // Итоговая формула: 2 * mx - sum
        cout << 2 * mx - sum << '\n'; // Выводим максимальный результат для текущего теста и переходим на новую строку
    }
    
    return 0; // Успешное завершение программы
}

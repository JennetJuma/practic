#include <iostream>  // Подключаем библиотеку для стандартного ввода и вывода (cin, cout)
#include <algorithm> // Подключаем библиотеку алгоритмов (нужна для использования функции min)

using namespace std; // Используем стандартное пространство имен, чтобы не писать std:: перед функциями

int main() {
    // Включаем оптимизацию потоков ввода-вывода для ускорения работы программы
    ios_base::sync_with_stdio(0); // Отключаем синхронизацию между потоками ввода-вывода C и C++
    cin.tie(0);                   // Отвязываем cin от cout, чтобы вывод не задерживал чтение

    int t; // Переменная для хранения количества наборов входных данных (тест-кейсов)
    // Считываем количество тестов. Если входные данные закончились или некорректны, завершаем программу
    if (!(cin >> t)) return 0;

    // Запускаем цикл, который последовательно обработает каждый тест-кейс (пока t уменьшается до нуля)
    while (t--) {
        int n, a, b; // n — нужное количество литров, a — цена 1 литра, b — цена 2-литровой бутылки
        cin >> n >> a >> b; // Считываем значения n, a и b для текущего теста

        // Сценарий 1: Покупаем все n литров только бутылками по 1 литру.
        // Стоимость будет равна количеству литров, умноженному на цену одной литровой бутылки.
        int cost1 = n * a;

        // Сценарий 2: Покупаем как можно больше 2-литровых бутылок.
        // (n / 2) — это максимальное количество 2-литровых бутылок, которое мы можем взять.
        // (n % 2) — остаток литров (будет 1, если n нечетное, и 0, если n четное).
        // Этот остаток мы вынуждены докупить по цене 'a' за одну литровую бутылку.
        int cost2 = (n / 2) * b + (n % 2) * a;

        // Сравниваем оба сценария с помощью функции min и выбираем самый выгодный вариант.
        // Выводим полученную минимальную стоимость и переходим на новую строку.
        cout << min(cost1, cost2) << "\n";
    }

    return 0; // Сигнализируем операционной системе об успешном завершении программы
}

#include <iostream> // Подключаем библиотеку для работы со стандартными потоками ввода-вывода (cin, cout)
#include <vector>   // Подключаем вектор (динамический массив) для хранения состояний ДП

using namespace std; // Используем стандартное пространство имен

int main() {
    // Включаем оптимизацию ввода-вывода для быстрого прохождения тестов на олимпиадах
    ios_base::sync_with_stdio(0); // Отключаем синхронизацию стандартных потоков C и C++
    cin.tie(0);                   // Отвязываем cin от cout, чтобы ускорить чтение данных

    int n, k; // n — целевая ступенька, k — максимальная длина одного прыжка
    // Считываем n и k. Если ввод пустой или некорректный — плавно завершаем программу
    if (!(cin >> n >> k)) return 0;

    // Задаем константу модуля для предотвращения целочисленного переполнения
    const int MOD = 1e9 + 7; // Число 1000000007, по которому берутся остатки в большинстве ДП-задач
    
    // Создаем массив ДП размера n + 1, изначально заполненный нулями.
    // dp[i] будет хранить количество способов добраться до i-й ступеньки.
    vector<int> dp(n + 1, 0);
    
    // Базовое состояние ДП: на нулевую (стартовую) ступеньку есть ровно 1 способ попасть — стоять там изначально
    dp[0] = 1;

    // Переменная для поддержания суммы ДП на скользящем отрезке длины k.
    // Используем long long, чтобы избежать переполнения при промежуточных математических операциях.
    long long current_sum = 0;

    // Итерируемся по каждой ступеньке от 1 до n включительно
    for (int i = 1; i <= n; i++) {
        // Чтобы сделать шаг на ступеньку 'i', мы могли прийти с предыдущей ступеньки 'i - 1'.
        // Добавляем количество способов для dp[i - 1] в наше скользящее окно (сумму) и берем по модулю.
        current_sum = (current_sum + dp[i - 1]) % MOD;
        
        // Окно должно хранить сумму только последних 'k' элементов: от dp[i - k] до dp[i - 1].
        // Если индекс 'i' строго больше 'k', значит элемент dp[i - k - 1] уже "выпал" из допустимого диапазона прыжка.
        if (i > k) {
            // Вычитаем самый старый элемент из суммы. 
            // Прибавляем MOD перед взятием остатка, так как разность (current_sum - dp[...]) может уйти в минус.
            current_sum = (current_sum - dp[i - k - 1] + MOD) % MOD;
        }

        // Количество способов попасть на текущую ступеньку 'i' равно сумме способов для 'k' предыдущих ступенек
        dp[i] = current_sum;
    }

    // После завершения цикла в dp[n] будет записан ответ для финальной n-й ступеньки
    cout << dp[n] << "\n"; // Выводим итоговый результат в консоль с переносом строки

    return 0; // Успешное завершение работы программы
}

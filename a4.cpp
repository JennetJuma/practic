#include <iostream>  // Подключаем библиотеку для базового ввода-вывода (cin, cout)
#include <algorithm> // Подключаем библиотеку алгоритмов (в данном коде не используется, но полезна для max/min)

using namespace std; // Используем стандартное пространство имен, чтобы не писать std:: перед командами

int main() {
    // Включаем оптимизацию ввода-вывода для ускорения работы программы в тестирующих системах
    ios_base::sync_with_stdio(0); // Отключаем синхронизацию между стандартными потоками C и C++
    cin.tie(0);                   // Отвязываем поток ввода cin от потока вывода cout

    int t; // Переменная для хранения количества наборов входных данных (тест-кейсов)
    // Считываем количество тестов. Если ввод пустой или некорректный, плавно завершаем программу
    if (!(cin >> t)) return 0;

    // Цикл обрабатывает каждый тест-кейс отдельно, пока переменная t уменьшается до нуля
    while (t--) {
        int a, b, c; // Объявляем три переменные для исходных чисел
        cin >> a >> b >> c; // Считываем значения a, b и c из стандартного ввода

        // Запускаем цикл ровно на 5 итераций, так как у нас есть ровно 5 единиц для распределения
        for (int i = 0; i < 5; i++) {
            // Проверяем, является ли число 'a' самым маленьким или равным остальным
            if (a <= b && a <= c) {
                a++; // Если 'a' минимально, увеличиваем его на 1
            } 
            // Если 'a' не минимально, проверяем, является ли число 'b' самым маленьким или равным 'c'
            else if (b <= a && b <= c) {
                b++; // Если 'b' минимально, увеличиваем его на 1
            } 
            // Если ни 'a', ни 'b' не оказались минимальными, значит, самое маленькое число — это 'c'
            else {
                c++; // Увеличиваем число 'c' на 1
            }
        }

        // После того как мы оптимально распределили все 5 единиц, 
        // выводим итоговое максимальное произведение чисел a, b и c, а затем переходим на новую строку
        cout << a * b * c << "\n";
    }

    return 0; // Сигнализируем операционной системе об успешном завершении программы
}

#include <bits/stdc++.h> // Подключаем все стандартные библиотеки C++
using namespace std;

// Функция для вычисления префикс-функции (массива pi) для строки s.
// pi[i] — это длина наибольшего собственного префикса подстроки s[0..i], который одновременно является её суффиксом.
vector<int> prefix_function(const string& s) {
    int n = (int)s.size();  // Получаем длину строки s
    vector<int> pi(n);       // Создаем вектор размера n, заполненный нулями, для хранения значений префикс-функции
    
    // Цикл начинается с 1, так как для первого символа (индекс 0) префикс-функция всегда равна 0
    for (int i = 1; i < n; ++i) {
        int j = pi[i - 1];   // Инициализируем длину предыдущего подходящего префикса
        
        // Пока символы не совпадают и нам есть куда отступать (j > 0), 
        // переходим к более короткому возможному префиксу-суффиксу
        while (j > 0 && s[i] != s[j]) {
            j = pi[j - 1];   // Откатываемся по значениям префикс-функции назад
        }
        
        // Если текущие символы совпали, то длина текущего префикса-суффикса увеличивается на 1
        if (s[i] == s[j]) {
            ++j;
        }
        
        pi[i] = j;           // Записываем финальное вычисленное значение для индекса i
    }
    return pi;               // Возвращаем полностью заполненный вектор pi
}

int main() {
    // Оптимизация стандартных потоков ввода-вывода для ускорения работы программы
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    string s, t;             // Объявляем две строки: s (исходная) и t (целевая)
    cin >> s >> t;           // Считываем строки s и t из стандартного ввода

    int n = (int)s.size();   // Запоминаем длину исходной строки s
    
    // Если длины строк не совпадают, то из s невозможно получить t с помощью сдвигов
    if ((int)t.size() != n) {
        cout << -1 << '\n';  // Выводим -1 (решение невозможно)
        return 0;            // Завершаем выполнение программы
    }

    // Удваиваем строку s. В s + s гарантированно содержатся ВСЕ возможные циклические сдвиги строки s
    string doubled = s + s;
    
    // Вычисляем префикс-функцию для ЦЕЛЕВОЙ строки t (она будет выступать шаблоном для поиска)
    vector<int> pi = prefix_function(t);

    int j = 0;               // Указатель (счетчик совпавших символов) для шаблона t
    int best_pos = -1;       // Переменная для хранения индекса самого последнего (максимального) вхождения t в doubled

    // КМП-поиск: бежим по удвоенной строке doubled и ищем вхождения шаблона t
    for (int i = 0; i < (int)doubled.size(); ++i) {
        // Если очередной символ doubled[i] не совпадает с ожидаемым символом шаблона t[j],
        // сдвигаем шаблон вправо, используя информацию из префикс-функции pi
        while (j > 0 && doubled[i] != t[j]) {
            j = pi[j - 1];   // Откат указателя шаблона назад без потери уже проверенных совпадений
        }
        
        // Если символы совпали, продвигаемся по шаблону t на один символ вперед
        if (doubled[i] == t[j]) {
            ++j;
        }

        // Если j достигло длины n, значит, мы успешно нашли полное вхождение строки t в doubled
        if (j == n) {
            int pos = i - n + 1;       // Вычисляем индекс начала этого вхождения в строке doubled
            best_pos = max(best_pos, pos); // Запоминаем максимальный индекс начала вхождения
            j = pi[j - 1];             // Сдвигаем указатель шаблона через префикс-функцию, чтобы продолжить поиск (возможны наложения)
        }
    }

    // Если ни одного вхождения строки t в doubled так и не было найдено
    if (best_pos == -1) {
        cout << -1 << '\n';            // Выводим -1
    } else {
        // Формула (n - best_pos) % n вычисляет количество необходимых циклических сдвигов вправо.
        // pos — это то, на сколько символов t смещена в doubled. Сдвиг строки s равен дополнению до длины n.
        // Операция % n обрабатывает случай, когда best_pos == 0 (строки изначально равны, сдвиг 0).
        cout << (n - best_pos) % n << '\n'; // Выводим итоговый ответ
    }

    return 0; // Успешный выход из программы
}

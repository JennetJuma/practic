#include <iostream> // Подключаем стандартный поток ввода-вывода (cin, cout)
#include <vector>   // Подключаем вектор (динамический массив)

using namespace std; // Используем стандартное пространство имен

int main() {
    // Включаем оптимизацию ввода-вывода для быстрого прохождения тестов
    ios_base::sync_with_stdio(0); // Отключаем синхронизацию между потоками ввода-вывода C и C++
    cin.tie(0);                   // Отвязываем cin от cout для ускорения работы

    int n, m, k; // n — количество строк сетки, m — количество столбцов, k — количество заблокированных клеток
    // Считываем размеры сетки и количество препятствий. Если ввод пустой или некорректный — завершаем программу
    if (!(cin >> n >> m >> k)) return 0;

    // Создаем двумерную матрицу смежности (вектор векторов) размером (n + 1) на (m + 1), изначально вся заполненная false.
    // bad[i][j] станет true, если клетка (i, j) заблокирована и наступать на неё нельзя.
    vector<vector<bool>> bad(n + 1, vector<bool>(m + 1, false));
    
    // Считываем координаты всех k заблокированных клеток
    for (int i = 0; i < k; i++) {
        int x, y; // Координаты строки и столбца препятствия
        cin >> x >> y;
        bad[x][y] = true; // Отмечаем эту клетку в матрице как "плохую"
    }

    // Задаем константу модуля для предотвращения переполнения типа int при сложении путей
    const int MOD = 1e9 + 7; // Число 1000000007, по которому часто просят выводить ответ в олимпиадных задачах
    
    // Создаем одномерный массив ДП для текущей строки. 
    // dp[j] хранит количество способов дойти до j-го столбца текущей строки.
    vector<int> dp(m + 1, 0);
    
    // Базовое состояние ДП: в стартовую клетку (0, 0) есть ровно 1 способ попасть — просто изначально находиться там
    dp[0] = 1;

    // Внешний цикл: перебираем строки сетки от 0 до n включительно
    for (int i = 0; i <= n; i++) {
        // Внутренний цикл: перебираем столбцы сетки от 0 до m включительно
        for (int j = 0; j <= m; j++) {
            
            // Если текущая клетка заблокирована (находится в списке bad)
            if (bad[i][j]) {
                dp[j] = 0; // Способов попасть сюда или пройти через неё нет, зануляем значение ДП для этого столбца
            } else {
                // Если клетка проходимая и мы находимся не в самом левом столбце (j > 0),
                // то мы можем прийти в клетку (i, j) из левой соседней клетки (i, j - 1).
                if (j > 0) {
                    // Значение dp[j] до этой строчки хранит количество способов прийти СВЕРХУ (из клетки i-1, j).
                    // Мы прибавляем к нему значение dp[j - 1], которое хранит количество способов прийти СЛЕВА (из клетки i, j-1).
                    // Также берем результат по модулю MOD, чтобы избежать переполнения.
                    dp[j] = (dp[j] + dp[j - 1]) % MOD;
                }
                // Если j == 0 (самый левый столбец), то прийти слева нельзя. 
                // В этом случае dp[0] просто сохраняет своё старое значение, пришедшее сверху (из dp[0] предыдущей строки i-1).
            }
        }
    }

    // После завершения всех циклов в dp[m] будет находиться ответ для правого нижнего угла сетки (n, m)
    cout << dp[m] << "\n"; // Выводим итоговое количество путей и символ переноса строки

    return 0; // Успешное завершение программы
}
